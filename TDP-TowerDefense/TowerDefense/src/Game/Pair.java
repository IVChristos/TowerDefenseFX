package Game;

public class Pair {
	//Atributos de instancia.
	private int X;
	private int Y;
	
	public Pair() {
		
	}
	//Getters and setters
	public int getX() {
		return X;
	}
	public void setX(int x) {
		X = x;
	}
	public int getY() {
		return Y;
	}
	public void setY(int y) {
		Y = y;
	}

}
